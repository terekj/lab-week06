static inline void send_pixel(uint16_t px) {
    uint8_t bytes[2] = { px >> 8, px & 0xFF };
    spi_write_blocking(SPI_PORT, bytes, 2);
}

typedef uint16_t (*pixel_fn)(int r, int c);

static void send_frame(pixel_fn get_pixel) {
    gpio_put(PIN_CS, 0);
    for (int r = 0; r < N; r++)
        for (int c = 0; c < N; c++)
            send_pixel(get_pixel(r, c));
    gpio_put(PIN_CS, 1);
}

static uint16_t rot0  (int r, int c) { return sprite[r][c]; }
static uint16_t rot90 (int r, int c) { return sprite[N-1-c][r]; }
static uint16_t rot180(int r, int c) { return sprite[N-1-r][N-1-c]; }
static uint16_t rot270(int r, int c) { return sprite[c][N-1-r]; }

static pixel_fn orientations[4] = { rot0, rot90, rot180, rot270 };
static const char *orientation_names[4] = { "0", "90 CW", "180", "270 CW" };


enum rot_state { SM_Start, SM_0deg, SM_90deg, SM_180deg, SM_270deg };
static enum rot_state state = SM_Start;
static struct repeating_timer timer;

static bool Tick(struct repeating_timer *t) {
    // state transitions
    switch (state) {
        case SM_Start:  state = SM_0deg;   break;
        case SM_0deg:   state = SM_90deg;  break;
        case SM_90deg:  state = SM_180deg; break;
        case SM_180deg: state = SM_270deg; break;
        case SM_270deg: state = SM_0deg;   break;
        default: break;
    }

    // state actions
    switch (state) {
        case SM_Start: break;
        case SM_0deg:   send_frame(orientations[0]); printf("Ex3: %s deg\n", orientation_names[0]); break;
        case SM_90deg:  send_frame(orientations[1]); printf("Ex3: %s deg\n", orientation_names[1]); break;
        case SM_180deg: send_frame(orientations[2]); printf("Ex3: %s deg\n", orientation_names[2]); break;
        case SM_270deg: send_frame(orientations[3]); printf("Ex3: %s deg\n", orientation_names[3]); break;
        default: break;
    }

    return true;
}

int main(void) {
    stdio_init_all();
    sleep_ms(2000);

    spi_init(SPI_PORT, 1 * 1000 * 1000);
    gpio_set_function(PIN_SCK,  GPIO_FUNC_SPI);
    gpio_set_function(PIN_MOSI, GPIO_FUNC_SPI);

    gpio_init(PIN_CS);
    gpio_set_dir(PIN_CS, GPIO_OUT);
    gpio_put(PIN_CS, 1);

    add_repeating_timer_ms(-1000, Tick, NULL, &timer);

    while (true) tight_loop_contents();
}
